function getAverage(scores)  { // Function to calculate the average of an array of scores
  let sum = 0; 
  let pembagi = scores.length;
  for(let i = 0; i < pembagi; i++){
    sum = sum + scores[i];
    
  }
  return sum/pembagi
}

